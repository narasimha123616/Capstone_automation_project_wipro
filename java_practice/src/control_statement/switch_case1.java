package control_statement;

public class switch_case1 {
	 public static void main(String[] args) {
	        int size = 28;  // Try changing this value

	       

	        switch (size) {
	            case 26 : System.out.println("Extra Small");
	            break;
	            case 28 : System.out.println("Small");
	            break;
	            case 30 : System.out.println("Meduim");
	            break;
	            case 32 : System.out.println("large");
	            break;
	            case 34 : System.out.println("Extra large");
	            break;
	            default:
	            	System.out.println("Invalid");
	        }

	      
}
}
	//like 26=extra small
	//* 28=small size
	//* 30=medium
	//* 32=large
	//* 34=Extra large
